public class OperadoresRelacionaisLogicos{
    public static void main(String[] args) {

        // - Relacionais
        // Igual (==)
        // Diferente (!=)
        // Maior (>)
        // Menor (<)
        // Maior ou Igual (>=)
        // Menor ou Igual (<=)

        // - LÃ³gicos 
        // && (e)
        // || (ou)

        double num1 = 10;
        double num2 = 3;
        double num3 = 5;





        
        System.out.println (num1 > num2);
        System.out.println (num1 < num2);
        System.out.println (num1 >= num2);
        System.out.println (num1 <= num2);
        System.out.println (num1 == num2);
        System.out.println (num1 != num2);
        System.out.println (num1 > num2 && num2 > num3);
        System.out.println (num1 > num2 || num2 > num3);


    }
}